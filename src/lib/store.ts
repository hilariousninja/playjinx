// --- Answers ---
export async function hasSubmitted(promptId: string): Promise<boolean> {
  const sid = getSessionId();
  const { count, error } = await supabase
    .from('answers')
    .select('*', { count: 'exact', head: true })
    .eq('prompt_id', promptId)
    .eq('session_id', sid);
  if (error) throw error;
  return (count ?? 0) > 0;
}

