export async function submitAnswer(promptId: string, rawAnswer: string): Promise<DbAnswer> {
  const sid = getSessionId();
  const { data, error } = await supabase
    .from('answers')
    .insert({
      prompt_id: promptId,
      session_id: sid,
      raw_answer: rawAnswer,
      normalized_answer: normalizeAnswer(rawAnswer),
    })
    .select()
    .single();
  if (error) throw error;
  return data;
}

export interface AnswerStat {
  normalized_answer: string;
  count: number;
  percentage: number;
  rank: number;
}

export async function getStats(promptId: string): Promise<AnswerStat[]> {
  const { data, error } = await supabase
    .from('answers')
    .select('normalized_answer')
    .eq('prompt_id', promptId);
  if (error) throw error;

  const counts: Record<string, number> = {};
  for (const row of data ?? []) {
    counts[row.normalized_answer] = (counts[row.normalized_answer] || 0) + 1;
  }

  const total = Object.values(counts).reduce((s, c) => s + c, 0);
  return Object.entries(counts)
    .sort((a, b) => b[1] - a[1])
    .map(([word, count], i) => ({
      normalized_answer: word,
      count,
      percentage: total > 0 ? Math.round((count / total) * 100) : 0,
      rank: i + 1,
    }));
}

