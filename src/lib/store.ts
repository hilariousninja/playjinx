export function getSessionId(): string {
  let sid = localStorage.getItem('jinx_session_id');
  if (!sid) {
    sid = 'sess_' + Math.random().toString(36).slice(2);
    localStorage.setItem('jinx_session_id', sid);
  }
  return sid;
}

// --- Prompts ---
export async function getActivePrompts(): Promise<DbPrompt[]> {
  const { data, error } = await supabase
    .from('prompts')
    .select('*')
    .eq('active', true)
    .order('created_at');
  if (error) throw error;
  return data ?? [];
}

// Ensure daily prompts exist — calls edge function if none found
export async function ensureDailyPrompts(): Promise<DbPrompt[]> {
  let prompts = await getActivePrompts();
  if (prompts.length >= 3) return prompts;

  // Call edge function to generate prompts
  try {
    const { data, error } = await supabase.functions.invoke('generate-daily-prompts');
    if (!error) {
      // Re-fetch after generation
      prompts = await getActivePrompts();
    }
  } catch {
    // Silently fail — prompts may be generated by cron
  }
  return prompts;
}

